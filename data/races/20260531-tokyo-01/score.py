def norm(score, conf):
    n = (score + 2) / 4.0
    if conf == '低':
        n = 0.5 + (n - 0.5) * 0.7
    return n

res = {}
for no, d in H.items():
    nA = norm(*d['A']); nB = norm(*d['B']); nC = norm(*d['C'])
    nD = norm(*d['D']); nE = norm(*d['E'])
    nF = norm(*d['F']); nG = norm(*d['G']); nH = 0.5  # H未収集=中立
    base = 100 * (0.40*nA + 0.40*nB + 0.20*nC)
    apt  = 1.0 + 0.15*(2*nD - 1) + 0.10*(2*nE - 1)
    cond = 1.0 + 0.07*(2*nF - 1) + 0.05*(2*nG - 1) + 0.05*(2*nH - 1)
    eI = d['I'][0]
    discount = base * 0.08 * (-eI)
    ability = max(0.0, base*apt*cond - discount)
    res[no] = dict(name=d['name'], base=base, apt=apt, cond=cond, disc=discount, ability=ability)

# softmax 勝率 (T=14)
T = 14.0
Z = sum(math.exp(r['ability']/T) for r in res.values())
for no, r in res.items():
    r['p'] = math.exp(r['ability']/T) / Z

# Harville 複勝率(top3)
ps = {no: r['p'] for no, r in res.items()}
def place3(i):
    pi = ps[i]
    p1 = pi
    p2 = sum(ps[k]*pi/(1-ps[k]) for k in ps if k!=i)
    p3 = 0.0
    for k in ps:
        if k==i: continue
        for l in ps:
            if l==i or l==k: continue
            p3 += ps[k]*ps[l]/(1-ps[k])*pi/(1-ps[k]-ps[l])
    return p1+p2+p3
for no, r in res.items():
    r['p3'] = place3(no)

# 偶然レンジ: 低確信度割合(8観点中Hも欠損)で広めに
for no, d in H.items():
    confs = [d[x][1] for x in ['A','B','C','D','E','F','G','I']]
    low_frac = (confs.count('低') + 1) / 9.0  # +1=H欠損
    sigma = res[no]['p'] * (0.15 + 0.10*low_frac + 0.05)  # 頭数16>14で+0.05
    res[no]['lo'] = max(0.0, res[no]['p']-sigma)
    res[no]['hi'] = res[no]['p']+sigma

order = sorted(res, key=lambda n: -res[n]['ability'])
print(f"{'No':>2} {'馬名':<10} {'base':>6} {'apt':>5} {'cond':>5} {'disc':>5} {'abil':>6} {'勝率':>6} {'レンジ':>13} {'複勝率':>6}")
for no in order:
    r = res[no]
    print(f"{no:>2} {r['name']:<10} {r['base']:6.1f} {r['apt']:5.3f} {r['cond']:5.3f} {r['disc']:5.1f} {r['ability']:6.1f} {r['p']*100:5.1f}% {r['lo']*100:5.1f}-{r['hi']*100:4.1f}% {r['p3']*100:5.1f}%")
print(f"\n勝率合計={sum(r['p'] for r in res.values())*100:.1f}%")
